// 按 layer 字段获取 Agent（基于扩展字段 layer，回退到分类映射）
export function getAgentsByLayer(layer: 'L1' | 'L2' | 'L3' | 'L4' | 'L5' | 'SP'): Agent[] {
  // 优先使用 layer 字段（新 Agent 体系）
  const layered = agents.filter(a => a.layer === layer)
  if (layered.length > 0) {
    // 合并：layer 字段命中 + 分类映射命中（去重）
    const categoryMap: Record<string, string> = {
      L1: 'L1 编排调度',
      L2: 'L2 交付执行',
      L3: 'L3 数据底座',
      L4: 'L4 治理安全',
      L5: 'L5 行业专家',
      SP: 'SP 特殊智能',
    }
    const byCategory = getAgentsByCategory()[categoryMap[layer]] || []
    const seen = new Set(layered.map(a => a.id))
    const merged = [...layered]
    for (const a of byCategory) {
      if (!seen.has(a.id)) {
        merged.push(a)
        seen.add(a.id)
      }
    }
    return merged
  }
  // 回退：仅按分类映射
  const categoryMap: Record<string, string> = {
    L1: 'L1 编排调度',
    L2: 'L2 交付执行',
    L3: 'L3 数据底座',
    L4: 'L4 治理安全',
    L5: 'L5 行业专家',
    SP: 'SP 特殊智能',
  }
  return getAgentsByCategory()[categoryMap[layer]] || []
}

// 按层级/分类统计 Agent 数量
export function getAgentStats(): {
  total: number
  byLayer: { layer: string; layerName: string; count: number }[]
  byCategory: { category: string; count: number }[]
} {
  const categories = getAgentsByCategory()
  const layerNameMap: Record<string, string> = {
    'L1 编排调度': 'L1 编排调度',
    'L2 交付执行': 'L2 交付执行',
    'L3 数据底座': 'L3 数据底座',
    'L4 治理安全': 'L4 治理安全',
    'L5 行业专家': 'L5 行业专家',
    'SP 特殊智能': 'SP 特殊智能',
  }

  const byLayer = Object.entries(categories).map(([category, catAgents]) => ({
    layer: category.split(' ')[0],
    layerName: layerNameMap[category] || category,
    count: catAgents.length,
  }))

  // 按 category 字段（新 Agent 体系的细分分类）统计
  const categoryCount: Record<string, number> = {}
  for (const agent of agents) {
    const cat = agent.category || '未分类'
    categoryCount[cat] = (categoryCount[cat] || 0) + 1
  }
  const byCategory = Object.entries(categoryCount).map(([category, count]) => ({
    category,
    count,
  }))

  return {
    total: agents.length,
    byLayer,
    byCategory,
  }
}

// 多字段检索 Agent（id/name/role/specialty/skills/description/capabilities/knowledgeTags）
export function searchAgents(query: string): Agent[] {
  const lower = query.trim().toLowerCase()
  if (!lower) return []

  const scored: { agent: Agent; score: number }[] = []

  for (const agent of agents) {
    let score = 0

    // id 精确匹配
    if (agent.id.toLowerCase() === lower) score += 20
    else if (agent.id.toLowerCase().includes(lower)) score += 10

    // 名称匹配
    if (agent.name.toLowerCase().includes(lower)) score += 15
    if (agent.name.includes(query.trim())) score += 8

    // 角色匹配
    if (agent.role.toLowerCase().includes(lower)) score += 8

    // 专长匹配
    if (agent.specialty.toLowerCase().includes(lower)) score += 8
    if (agent.specialty.includes(query.trim())) score += 5

    // 描述匹配
    if (agent.description.toLowerCase().includes(lower)) score += 5
    if (agent.description.includes(query.trim())) score += 3

    // 技能匹配
    for (const skill of agent.skills) {
      if (skill.toLowerCase().includes(lower)) score += 4
      if (skill.includes(query.trim())) score += 2
    }

    // capabilities 匹配（新 Agent）
    if (agent.capabilities) {
      for (const cap of agent.capabilities) {
        if (cap.toLowerCase().includes(lower)) score += 4
        if (cap.includes(query.trim())) score += 2
      }
    }

    // knowledgeTags 匹配（新 Agent）
    if (agent.knowledgeTags) {
      for (const tag of agent.knowledgeTags) {
        if (tag.toLowerCase().includes(lower)) score += 4
        if (tag.includes(query.trim())) score += 2
      }
    }

    // tools 匹配（新 Agent）
    if (agent.tools) {
      for (const tool of agent.tools) {
        if (tool.toLowerCase().includes(lower)) score += 3
      }
    }

    // layer / category 匹配
    if (agent.layer && agent.layer.toLowerCase() === lower) score += 6
    if (agent.category && agent.category.toLowerCase().includes(lower)) score += 5

    if (score > 0) {
      scored.push({ agent, score })
    }
  }

  scored.sort((a, b) => b.score - a.score)
  return scored.map(s => s.agent)
}

